string=input("Enter a string:")
print(string[::-1]) #Reverse
print(string[0:11]) #from 0 index to index value 10
print(string[:10])  #from 0 index to index value 9
print(string[5:])   #from 5 index to last index value
print(string[0::2])  #from index 0 to last index value and leaving 1 character in between
